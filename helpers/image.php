function getFoodImage(?string $uploadedImage, string $itemName = '', string $categoryName = ''): string {
    if (!empty($uploadedImage)) {
        return $uploadedImage;
    }
    $nameLower = strtolower($itemName);
    foreach (FOOD_IMAGES as $keyword => $url) {
        if (str_contains($nameLower, $keyword)) {
            return $url;
        }
    }
    foreach (CATEGORY_IMAGES as $cat => $url) {
        if (stripos($categoryName, $cat) !== false || stripos($cat, $categoryName) !== false) {
            return $url;
        }
    }
    return DEFAULT_FOOD_IMAGE;
}

function getRestaurantBanner(array $restaurant): string {
    return !empty($restaurant['banner_image'])
        ? $restaurant['banner_image']
        : (!empty($restaurant['image']) ? $restaurant['image'] : DEFAULT_FOOD_IMAGE);
}

function getRestaurantLogo(array $restaurant): string {
    return !empty($restaurant['logo_image'])
        ? $restaurant['logo_image']
        : (!empty($restaurant['image']) ? $restaurant['image'] : DEFAULT_FOOD_IMAGE);
}

function getCategoryEmoji(string $name): string {
    $map = [
        'pizza'    => '🍕', 'burger'  => '🍔', 'sushi'   => '🍣',
        'pasta'    => '🍝', 'dessert' => '🍰', 'healthy' => '🥗',
        'drink'    => '🥤', 'biryani' => '🍛', 'chinese' => '🥡',
        'indian'   => '🍲', 'street'  => '🌮', 'seafood' => '🦐',
        'breakfast'=> '🥞', 'south'   => '🫓', 'chicken' => '🍗',
        'snack'    => '🍟', 'coffee'  => '☕', 'sandwich'=> '🥪',
    ];
    $lower = strtolower($name);
    foreach ($map as $key => $emoji) {
        if (str_contains($lower, $key)) return $emoji;
    }
    return '🍽️';
}
